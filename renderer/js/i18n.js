  function t(key, vars) {
    const table = DICT[lang] || DICT.en;
    let s = (key in table) ? table[key] : (key in DICT.en ? DICT.en[key] : key);
    if (vars) s = s.replace(/\{(\w+)\}/g, (m, k) => (k in vars ? vars[k] : m));
    return s;
  }

  function setLang(l) {
    if (LANGS.includes(l)) lang = l;
  }
  function getLang() { return lang; }

  // Translate all [data-i18n] (textContent) and [data-i18n-ph] (placeholder) nodes.
  function apply(root) {
    (root || document).querySelectorAll('[data-i18n]').forEach((el) => {
      el.textContent = t(el.getAttribute('data-i18n'));
    });
    (root || document).querySelectorAll('[data-i18n-ph]').forEach((el) => {
      el.setAttribute('placeholder', t(el.getAttribute('data-i18n-ph')));
    });
  }

  window.I18N = { t, setLang, getLang, apply, LANGS, APP_VERSION };
